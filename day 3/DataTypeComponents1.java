public class DataTypeComponents1
{
	public static void main(String[] args)
	{
	   System.out.println("the datatype  is : float");
       System.out.println("the default value  is : 0.0f");
	   System.out.println("the size  is : "+Float.SIZE+" bits");		
       System.out.println("the range  is : "+Float.MIN_VALUE +" to "+Float.MAX_VALUE);
	   System.out.println();
	   System.out.println("the datatype  is : double");
       System.out.println("the default value  is : 0.0d");
	   System.out.println("the size  is : "+Double.SIZE+" bits");		
       System.out.println("the range  is : "+Double.MIN_VALUE +" to "+Double.MAX_VALUE);
	   System.out.println();
	}
}