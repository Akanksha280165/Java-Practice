public class DataTypeComponents
{
	public static void main(String[] args)
	{
       System.out.println("the datatype  is : byte");
       System.out.println("the default value  is : 0");
	   System.out.println("the size  is : "+Byte.SIZE+" bits");		
       System.out.println("the range  is : "+Byte.MIN_VALUE +" to "+Byte.MAX_VALUE);
	   System.out.println();

       System.out.println("the datatype  is : short");
       System.out.println("the default value  is : 0");
	   System.out.println("the size  is : "+Short.SIZE+" bits");		
       System.out.println("the range  is : "+Short.MIN_VALUE +" to "+Short.MAX_VALUE);
	   System.out.println();
	   

       System.out.println("the datatype  is : int");
       System.out.println("the default value  is : 0");
	   System.out.println("the size  is : "+Integer.SIZE+" bits");		
       System.out.println("the range  is : "+Integer.MIN_VALUE +" to "+Integer.MAX_VALUE);	   
      	
	   
	}
}